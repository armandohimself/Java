package com.armando.sandbox.CodingExercises.ControlFlow;

public class loopingStructures {
    public static void main(String[] args) {
        int x = 10;

        while( ++x < 20) {
            System.out.println("Value of x after: " + x);
            System.out.println("new line \n");
            
        }
        
        System.out.println("Value of x now after the while is: " + x);

    }
    
}
